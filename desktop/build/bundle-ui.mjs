// bundle-ui.mjs — assemble the desktop renderer HTML from the canonical Apps
// Script UI source (../../src/ui). This reproduces what Apps Script's doGet +
// include() do at request time, but produces one static document per view, with
// the server-injected bootstrap removed (the Electron preload provides
// window.FSW) and the gcall transport left to detect window.fswApi at runtime.
//
// Re-run whenever you change anything under src/ui:  npm run bundle
//
// String replacements use function replacers so a literal "$" in CSS/JS is never
// interpreted as a regex replacement token.

import { readFileSync, writeFileSync, mkdirSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const here = dirname(fileURLToPath(import.meta.url));
const UI = join(here, '..', '..', 'src', 'ui');
const OUT = join(here, '..', 'renderer');

const read = (f) => readFileSync(join(UI, f), 'utf8');

const styles = read('Styles.html');
const jsCommon = read('JsCommon.html');
const views = {
  manager:  { markup: read('Manager.html'),  script: read('JsManager.html') },
  employee: { markup: read('Employee.html'), script: read('JsEmployee.html') },
  notice:   { markup: read('Notice.html'),   script: '' }
};

let base = read('Index.html');

// 1. Drop the Sites-only iframe escape hatch (no iframe on the desktop).
base = base.replace(/[ \t]*<base target="_top">.*\n/, '');
// 2. Inline the stylesheet into <head>.
base = base.replace("<?!= include('ui/Styles'); ?>", () => styles);
// 3. Remove the server bootstrap <script> — the preload supplies window.FSW.
base = base.replace(
  /<script>\s*\/\/ Server-provided[\s\S]*?<\/script>/,
  () => '<!-- bootstrap is provided by the Electron preload as window.FSW -->'
);
// 4. Inline the shared client script (its gcall auto-detects the desktop bridge).
base = base.replace("<?!= include('ui/JsCommon'); ?>", () => jsCommon);
// 5. Strip the two server-side conditional view-script blocks; we inject per build.
base = base.replace(/<\?\s*if \(viewFile === 'ui\/Employee'\)[\s\S]*?<\?\s*\}\s*\?>\s*/g, () => '');
base = base.replace(/<\?\s*if \(viewFile === 'ui\/Manager'\)[\s\S]*?<\?\s*\}\s*\?>\s*/g, () => '');

const banner = '<!-- GENERATED by desktop/build/bundle-ui.mjs from src/ui/* — do not edit by hand. -->\n';

mkdirSync(OUT, { recursive: true });

let unresolved = 0;
for (const [view, v] of Object.entries(views)) {
  let html = base.replace("<?!= include(viewFile); ?>", () => v.markup);
  const tail = v.script ? ('\n' + v.script + '\n  ') : '';
  html = html.replace('</body>', () => tail + '</body>');
  html = banner + html;

  // Safety net: no Apps Script template tags should survive into a static file.
  const leftovers = html.match(/<\?[\s\S]*?\?>/g);
  if (leftovers) {
    unresolved += leftovers.length;
    console.error(`  ⚠ ${view}.html: ${leftovers.length} unresolved template tag(s): ` +
      leftovers.slice(0, 3).map((s) => s.slice(0, 40)).join(' | '));
  }

  writeFileSync(join(OUT, view + '.html'), html, 'utf8');
  console.log(`  wrote renderer/${view}.html (${html.length} bytes)`);
}

if (unresolved) {
  console.error(`\n✗ ${unresolved} unresolved template tag(s) — the UI source may have changed shape.`);
  process.exit(1);
}
console.log('✓ UI bundle complete.');
